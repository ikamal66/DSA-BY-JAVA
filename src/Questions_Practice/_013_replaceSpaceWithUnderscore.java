package Questions_Practice;
/*
    Problem :Write a java program to replace spaces with underscores.
 */
public class _013_replaceSpaceWithUnderscore {
    public static void main(String[] args) {
        String str = "Her name is  Shefali";
        System.out.println(str.replace(" ","_"));
    }
}
/*
Output:
Her_name_is__Shefali
 */
