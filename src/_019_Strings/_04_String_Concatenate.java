package _019_Strings;
// Concatenation in String
public class _04_String_Concatenate {
    public static void main(String[] args) {
        // Concatenate means, 2 cheezo ko add karna
        String firstName = "Kamal";
        String middleName = "Singh";
        String fullName = firstName + " " + middleName; // this is called concatenation
        System.out.println(fullName);
    }
}
/*
Output:
Kamal Singh
 */